                </p>

                <h6>Functionele en analytische cookies</h6>

                <p>

                    Mijn blog plaatst deze cookies door gebruik te maken van onder andere ‘Google Analytics’.
                    Door ‘Google Analytics’ krijgen we statistieken van onze website.
                    Deze gegevens proberen we zo goed mogelijk te analyseren en vervolgens besluiten we in hoeverre
                    we onze website of onze artikelen nog meer afstemmen op de lezers.
                    In principe is ons uitgangspunt dat we vooral schrijven over de dingen waar we graag over willen schrijven.
                    Maar we proberen daarbij bijvoorbeeld wel goede titels te bedenken die onze lezers aanspreken.
                    Daarnaast letten we er bijvoorbeeld ook op dat onze website op verschillende apparaten goed te lezen is.
                    Enkel hiervoor worden de statistieken gebruikt.
                    De gegevens worden absoluut niet aan derden ter beschikking gesteld.
                    Het gaat hierbij om onder andere de volgende gegevens:

                    <ul class="list-group">
                        <li class="list-group-item">
                           De internetbrowser die wordt gebruikt (bijvoorbeeld Internet Explorer of Firefox).
                        </li>
                        <li class="list-group-item">
                            Het aantal bezoekers per dag of per maand.
                        </li>
                        <li class="list-group-item">
                            Het tijdstip van het bezoek.
                        </li>
                        <li class="list-group-item">
                            De duur van het bezoek.
                        </li>
                        <li class="list-group-item">
                            Welke pagina’s bezocht zijn.
                        </li>
                        <li class="list-group-item">
                            Wat voor soort apparaat wordt gebruikt (bijvoorbeeld een pc, smartphone of tablet).
                        </li>
                    </ul>

                    Bovendien willen we hierbij graag nog benadrukken dat we absoluut niet kunnen zien wie er precies onze website bezoekt.
                    We zie geen namen of adressen of IP-adressen.

                </p>

                <h6>Tracking cookies</h6>

                <p>

                    Op onze website plaatsen we veel artikelen die je via een ‘knop’ kunt delen via sociale netwerken.
                    Om die ‘knoppen’ goed te kunnen laten werken plaatsen de bedrijven van die sociale netwerken cookies zodat ze
                    jouw profiel kunnen herkennen wanneer je op dat sociale netwerk bent ingelogd.
                    Deze cookies vallen onder de tracking cookies.
                    Op onze website staan deel-knoppen van Facebook, Twitter en Instagram.
                    Voor informatie over de mogelijke data die zij hiermee verzamelen verwijzen we je door naar de privacyverklaringen
                    van deze partijen op hun eigen website.
                    We gebruiken de tracking cookies dus enkel om de lezer de mogelijkheid te geven onze artikelen te delen.
                    We hebben hierbij geen commercieel belang.
                    In het programma ‘Google Analytics’ maken we geen gebruik van marketing, rapportage of advertentie instellingen.
                    Die categorie tracking cookies zijn op onze website dus niet van toepassing.

                </p>

                <h6>Affiliate cookies</h6>

                <p>

                    a4dbredaoost gebruikt affiliate cookies.
                    Deze cookies verwijzen naar a4dbredaoost en er is hier dus geen sprake van privacyschending of een tracking cookie naar jou persoonlijk.
