                <h5>Cookie Wetgeving</h5>

                <p>

                    Op deze pagina probeer ik zo duidelijk mogelijk uit te leggen van welke cookies onze
                    website gebruik maakt en welk doel zij dienen.
                    Een beheerder van een website moet daar immers transparant in zijn.

                </p>

                <h5>Wat is een cookie?</h5>

                <p>

                    Cookies zijn kleine tekstbestandjes die websites tijdens of na jouw bezoek
                    op bijvoorbeeld je computer, smartphone of tablet plaatsen.
                    Dit kleine tekstbestandje omvat informatie die tijdens een later bezoek aan de
                    website weer wordt herkend door de betreffende website.

                </p>

                <h5>Waarom plaatst a4dbredaoost cookies?</h5>

                <p>

                    Dat doen we om te kunnen kijken hoe bezoekers onze website gebruiken en om te
                    kunnen zien welke artikelen veel of juist weinig gelezen worden.
                    Niet dat we daar van laat afhangen wat we of niet op onze website plaatsen.
                    Maar we probeer er wel van te leren door bijvoorbeeld betere titels te bedenken.
                    Ook kunnen we zien via welke links of kanalen bezoekers op onze website komen.
                    Ook hierbij geldt dat het niet het doel is van onze website om alleen maar bezig te
                    zijn met genereren van meer bezoekers.
                    Het is wel fijn om te weten wat wel en wat niet werkt zodat we weten waar we onze
                    tijd en energie in moeten stoppen.

                </p>

                <h5>Welke cookies gebruikt a4dbredaoost?</h5>

                <p>

                    Voor onze website maken we gebruik van verschillende soorten cookies.
